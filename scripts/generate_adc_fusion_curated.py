#  Generate ADC curated data (~150 entries)
# ═══════════════════════════════════════════════════════════════════════

def generate_adc_data(target_count=150, seed=100):
    rng = np.random.RandomState(seed)
    records = []

    # First: exact template sequences (11 templates)
    for template_name, hc, lc in ADC_TEMPLATES:
        rec = compute_record(
            f"curated_adc_{len(records):03d}_{template_name}",
            "adc", hc, lc, "curated_adc"
        )
        if rec:
            records.append(rec)

    # Then: mutated variants — cycle through templates
    variant_idx = 0
    while len(records) < target_count:
        template_name, hc, lc = ADC_TEMPLATES[variant_idx % len(ADC_TEMPLATES)]
        n_mut = rng.randint(1, 6)  # 1–5 mutations per chain
        hc_mut = mutate_sequence(hc, n_mut, rng)
        lc_mut = mutate_sequence(lc, rng.randint(1, 4), rng)
        rec = compute_record(
            f"curated_adc_{len(records):03d}_{template_name}_v{variant_idx}",
            "adc", hc_mut, lc_mut, "curated_adc"
        )
        if rec:
            records.append(rec)
        variant_idx += 1

    return records


