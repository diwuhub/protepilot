def mutate_sequence(seq: str, n_mutations: int, rng: np.random.RandomState) -> str:
    """Introduce n_mutations random AA substitutions into seq."""
    seq_list = list(seq)
    length = len(seq_list)
    positions = rng.choice(length, size=min(n_mutations, length), replace=False)
    for pos in positions:
        original = seq_list[pos]
        candidates = [a for a in AA if a != original]
        seq_list[pos] = rng.choice(candidates)
    return "".join(seq_list)


def compute_record(name, mol_class, hc_seq, lc_seq, source):
    """Build a full feature record dict."""
    combined = hc_seq + lc_seq if lc_seq else hc_seq
    feats = compute_sequence_features(combined)
    if not feats:
        return None
    n_chains = (1 if len(hc_seq) >= 20 else 0) + (1 if len(lc_seq) >= 20 else 0)
    return {
        "name": name,
        "molecule_class": mol_class,
        "hc_sequence": hc_seq,
        "lc_sequence": lc_seq,
        "n_chains": n_chains,
        "n_unique_chains": n_chains,
        "source": source,
        **feats,
    }


